#include "libft.h"

char	*ft_strdup(const char *src)
{
	char			*dst;
	size_t			i;
	size_t			len;

	len = ft_strlen(src);
	i = 0;
	if (!(dst = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	while (i < len)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
